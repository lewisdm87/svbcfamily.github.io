<p>The Faithfulness of God</p>

<p>Things just wear out don’t they? Light bulbs burn out, batteries die out, cars rust out and bodies wear out. While everything around us decays one thing is constant, God! He never burns out, wears down, rusts out or falls apart. Biblically we call Him the Alpha and the Omega, the beginning and the end, the one who was, and is, and is to come. In a word, FAITHFUL. God’s faithfulness can be defined as His constant and “loyal love” for us. Listen to Pastor and author Max Lucado on Gods faithfulness:</p>

<blockquote>
<p>”Father. Your love never ceases, Never. Though we spurn you, ignore you, disobey you, you will not change. Our evil cannot diminish your love. Our good ness cannot increase it. Our faith does not earn it any more than our stupidity jeopardizes it. You don’t love us less if we fail. You don’t love us more if we succeed” God is 100% faithful to His word, His promises, His people, and His character because He cannot be otherwise. He will never let you down!”</p>
</blockquote>

