<h2>June 18, 2020</h2>

<p>Subject: Pastor</p>

<p>MORNING EDITION June 18, 2020 By Mike Huckabee</p>

<p><em>Text edited for readability</em></p>

<p>With all of the crazy, disturbing news these days, it is very easy to lose sight of what is important and enduring. Today&#39;s newsletter is going to focus on these things...</p>

<p>THE SPIRITUAL SIDE OF OUR LIVES REALLY DOES MATTER</p>

<p>When I was growing up, my bedtime ritual always included a fairy tale that started with &quot;Once upon a time...&quot; and ended with the comforting words we all remember: &quot;And they lived happily every after.&quot; As a child of the optimistic 1950s, I dreamed that life might be like that: whatever obstacles, dangers, or perils might come my way, in the end, I would live happily ever after.</p>

<p>There were certainly plenty of struggles along the way, but I have to say that things did eventually work out even more happily than I could have imagined, from a career that I love to a wonderful family, including the world&#39;s greatest grandkids. But sadly, for many people, &quot;living happily ever after&quot; does seem like an unobtainable fairy tale. Why is that happy ending seemingly out of reach for so many people?</p>

<p>Of course, there are always factors beyond our control, like health problems and accidents. None of us can ever know if our birthday or Christmas celebration was the last we&#39;ll ever enjoy. We have no way of knowing when it will all end, only that someday, it will (that&#39;s why it&#39;s said that the only certainties in life are death and taxes.)</p>

<p>Well, I can&#39;t help you with your taxes, but I do have a bit of advice that I think will make death less frightening and greatly increase your chances of living &quot;happily ever after.&quot;</p>

<p>For decades, our nation has been focused on personal pleasure. The message drummed into everyone by pop culture is &quot;If it feels good, do it&quot; lt&#39;s fostered a culture of self-centeredness that led to Baby Boomers being nicknamed &quot;The Me Generation&quot; Today&#39;s young people have been dubbed &#39;iGen&#39; because many are so fixated on self and selfies that even their gadgets&#39; names all start with &#39;Mc:el:6E Advertising bombards us with the message that life is all about me and all about now. Such messages of immediate self-gratification may sell products and services, but they cause us to sell our souls if we follow this philosophy to its logical conclusion.</p>

<p>At some point in life, we all experience events that shake up our routine, much like the agitator in a washing machine shakes loose the grime in our clothes. We may not want or enjoy such experiences, but they are necessary to force us to focus on the frailty of life and the certainty of death. They also force us to begin asking what really matters and why.</p>

<p>If we react to setbacks based solely on what feels good right now, we greatly lower our chances of enjoying a happy future. But if we believe there is even a remote possibility that our actions have lasting implications beyond the immediate, both within and beyond our lifetimes, it should cause us to think differently, live differently, and leave a different kind of legacy.</p>

<p>Without apology, I believe that the spiritual side of our lives really does matter. To believe otherwise is to define humans as little more than animated protoplasm, going through the motions of life for no particular purpose. I prefer to believe there&#39;s more to us than flesh and blood. if we possess a soul capable of living beyond our lifetimes, then the seeds we plant in this life will yield fruit forever. If you believe those things, the ultimate becomes more important than the immediate.</p>

<p>When we decide to live beyond our lifetimes, our responsibilities to the next generation will outweigh our roles in our current jobs. More important than the money we&#39;re paid for our work is what we will become as a result of our work. Our character will become more important than the careers we follow.</p>

<p>For all of us, life began &#39;once upon a time.&#39; Unlike the fairy tales, however, it&#39;s up to us to make the choices that determine whether the last line of our life stories will read, &#39;And they lived happily ever after.&#39;</p>

<p>SELF-GOVERNMENT REQUIRES SELF-DISCIPLINE</p>

<p>There are a lot of things people like to believe that are patently absurd if you think about it. Much of the Obama Administration was based on making nonsensical declarations &quot;we can&quot; just drill our way out of an energy shortage, it would take a magic wand to bring back manufacturing jobs, 2 percent growth is the &#39;new normal&#39; etc. &quot;all delivered in a somber, imperious tone that made them sound like unassailable fact. One of the most common pieces of false conventional wisdom is that &#39;the government can legislate morality&#39;</p>

<p>But of course, they do it all the time. We have millions of laws, just to enforce society consensus of what&#39;s morally right or wrong. Liberals used to protest this, and now they&#39;re the chief generators of morality laws, usually bans on everything they find morally offensive, including smoking, using racist words (or the &#39;wrong&#39; pronoun), giving someone a plastic straw, etc. etc. etc. Each law comes with loopholes that someone will try to exploit, so the government adds more laws. Plus police, courts, and jails, because some people will always insist on doing the wrong thing anyway. All to legislate morality.</p>

<p>Self-government requires self-discipline, self-respect, and respect for others. When people don&#39;t follow an accepted standard moral code, the government keeps passing new laws to try to force them to, which creates a bigger government and more expense for everybody. Maybe the national debt wouldn&#39;t be sky high now if our behavior standards hadn&#39;t sunk so low.</p>

<p>How much do people&#39;s bad personal choices end up costing all the rest of us? You might be surprised at the size of the bill. When I left the governors&#39; office in Arkansas, we had more than 13,000 inmates in the Department of Corrections. Just keeping them locked up costs taxpayers more than $220 million a year. That&#39;s more than it would have cost to send 13,000 kids to any college in the state, all expenses paid. If every prison inmate had just lived a moral life and stayed out of trouble, the taxpayers could have enjoyed a $220 million tax cut. Or the money might have been used to improve roads and services that benefit everyone.</p>

<p>From the left, l&#39;d always hear that we should spend more money on prisoners or else turn more of them loose. From the right, l&#39;d hear that we should lock up more people and eliminate parole while cutting the prison budget. Both were unrealistic. But hardly anyone wanted to talk about the real problem: the lack of morality that led to all those people being locked up in the first place.</p>

<p>And what about juvenile offenders? Every kid placed into our Division of Youth Services cost taxpayers up to $80,000 a year. If they&#39;d all had stable, nurturing homes and been taught to be obedient, responsible, and moral, it would have saved the taxpayers of just that one state $80 million a year. Imagine how many parks we could have built for all kids to enjoy, or how many books we could have bought for school libraries if we could have freed up $80 million a year in the state budget.</p>

<p>A lot of kids get into trouble because of peer pressure. They think breaking the rules makes them look cool to their friends. So kids, when someone you know starts acting up, instead of rewarding them with your admiration, please tell them instead, &quot;That&#39;s not cool! Thanks for costing us our parks and turning our generation into tax slaves, jerks&quot;</p>

<p>Hey, as long as kids are going to be vulnerable to peer pressure, why not use its power for good?</p>

<p>HOW YOU DEFINE LIBERTY IS IMPORTANT</p>

<p>We hear often these days that society shouldn&#39;t have any absolute moral standards. Imposing standards is oppressive, judgmental and outdated. Like Linus and the Great Pumpkin, it doesn&#39;t matter what we choose to believe, as long as we&#39;re really sincere in believing it. Well, pardon me for pointing it out, but that&#39;s well, stupid (sorry, Linus!)</p>

<p>There are all sorts of absolute standards to which we adhere. A rock band might be filled with nonconformists, but they can&#39;t each play in whatever key they feel like. You might concede Hannibal Lecter&#39;s sincere belief in cannibalism, but you wouldn&#39;t go to his house for dinner. It seems counter-intuitive, but freedom can&#39;t work unless we all agree to abide by certain basic standards of right and wrong. When we step outside those boundaries, chaos ensues. That can leave a bad taste in your mouth, and l&#39;m not still referring to Hannibal Lecter. I like to illustrate the concept with a story from the days when my own kids were young.</p>

<p>When my son John Mark was only 12, he decided one day to bake a cake. My wife Janet and I returned home and were greeted by our son, proudly offering dad the first taste. Well, it looked good, and I was already preparing some fatherly praise as I took that first bite. But what came out of my mouth wasn&#39;t words. It was the cake. It was so awful, I had to spit it out. My first thought was that my son was trying to kill me for the insurance.</p>

<p>As soon as my tongue overcame its shock, I asked John Mark if he&#39;d used a recipe. He said he had, and he&#39;d followed it to the letter. Well, except that he didn&#39;t know what a &#39;dash of salt&#39; meant, so he decided a cup of salt should be enough.</p>

<p>Now, my son worked hard on that cake, he&#39;d had the best of intentions and he sincerely believed he&#39;d done a good job. But hard work, good intentions, and sincere beliefs meant nothing once he decided he could make up his own measurement standards. That&#39;s literally a recipe for disaster.</p>

<p>Freedom can&#39;t exist in a moral vacuum. It makes some people uncomfortable to hear this, but without clear boundaries of right and wrong, the very concept of liberty breaks down. A person might argue that he should be free to</p>

<p>look at pictures others find offensive. But if it&#39;s a photo of a child who&#39;s being exploited, then there&#39;s more at stake than just the liberty of the viewer. There was a big controversy over separating children from parents who crossed the border illegally, but very few people brought up the facts that the parents chose to bring their children along as they knowingly violated federal immigration law 6E&quot; or that (as a pilot DNA test program proved) in many cases, those weren&#39;t even parents but people exploiting children they weren&#39;t even related to.</p>

<p>Self-government can&#39;t mean each of us lives by our own unique set of rules. If that&#39;s how you define liberty, then you&#39;re just going to get less of it. When people live outside the boundaries of a principled and agreed-upon moral code, it always leads to a government that&#39;s bigger and more intrusive, just to force people to do the right thing. Not to mention creating an avalanche of lawsuits.</p>

<p>If you think more lawyers and bigger government actually improve society, then I have a delicious cake recipe l&#39;d like to sell you. Then again, taking your money for that cake recipe would definitely be morally wrong.</p>

<p>THE TYPE OF LEADERS WE NEED</p>

<p>We all knew a kid in school who just had to run everything. Remember the classmate who insisted on picking the games you&#39;d all play at recess, where you&#39;d go after school, even who was &#39;in or out&#39; of your group? In high school, that kid had a compulsive need to be the leader of every student organization. You just wanted to say, Hey! You are not the boss of me! Whatever happened to those kids? I wouldn&#39;t be surprised if most ended up in government. And as the recent Democratic Debates proved, a pretty large percentage of them are currently running for President.</p>

<p>I&#39;m convinced the world is divided into people who just want to live their own lives and those who, for some reason, have an uncontrollable urge to tell everyone else how to live. Unfortunately, to that latter group, the government seems like the ideal place to work. As more of them gravitated toward government, Congress abdicated much of its legislating authority to unelected bureaucrats. Their bureaus grew and grew, and so did their regulations with the force of law (but no input from the people.) Then one day, we looked up and discovered we had a crushing national debt and were paying huge salaries to an army of people who enforce how big your soda should be and who couldn&#39;t be fired. That&#39;s when sane people realized that government is the LAST place these control freaks needed to be.</p>

<p>The temptation for government to overreach is hardly new. In fact, it stretches back to the beginning of recorded history, and I bet even earlier than that. There&#39;s a story in the ninth chapter of the book of Judges in the Old Testament about Gideon&#39;s son Abimelech, who craved leadership and stature - not to serve the people but to control them and make them serve him. He said, &quot;Give me dominion over your lives, and I will simplify your existence.&quot; Wow, does that sound familiar? It&#39;s basically the entire 2020 Democratic platform. Our government has taken us pretty far down that same road, but does your life seem any simpler -- or just a lot less free?</p>

<p>Anyway, back to Abimelech. He had a very smart younger brother, Jotham, who came up with a clever tale about three trees: an olive tree, a fig tree, and a vine tree. All three were fine trees that produced lots of fruit. All were offered the exalted position of “Kind of All Trees” but all three turned it down. The plant that wanted to be “Kind of All Trees” was the bramble bush, a weak plant that produces no fruit at all. Jothama&#39;s point was that only the weak and nonproductive have the desire to rule everyone else.</p>

<p>When anyone aspires to a position of power, take a long, hard look. If that person seemingly crawled out of the cradle with an ambition to be President, then beware! Anytime someone talks about &quot;running the country,&quot; alarm bells should sound. No one AE&quot; not the President, not Congress, no one person &quot;runs the country.&quot; That&#39;s why the Founders took such pains to divide and limit federal power, and why we need to reinstate those limits that have been blurred in recent years, whether by Presidents ruling via executive order or out-of-control judges legislating from the bench or unelected bureaucrats trying to influence the results of elections. If we allow any one person or entity to ignore those limits and assume the power to run everything, we won&#39;t be able to stop them when they run America into the ground.</p>

<p>We should pick leaders who resemble the trees in the Bible story that don&#39;t need or crave power but that have shown they bear good fruit. As it is said, by their fruits ye shall know them. The government has more than enough nuts already.</p>

