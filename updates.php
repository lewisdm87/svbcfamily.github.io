<p>Subject: Pastor’s Devotional</p>

<p>THE GOODNESS OF GOD</p>

<p>Ps 84:11</p>

<p>Our world today seems to see God through the lens of circumstance. They tend to look at the violence, disease, cruelty, etc and state: “If God is too good, why do these things happen.” So, how would you answer that question? The explanation you are going to give is going to be based on how you see God in your own experience; how God has revealed himself to you in your life. Listen to the words and wisdom of A.W. Tozer:</p>

<p>“The goodness of God is that which disposes Him to be kind, cordial, benevolent, and full of good will toward men. He is tenderhearted and quick of sympathy, and His unfailing attitude toward all moral beings is open, frank, and friendly. By His nature, He is inclined to bestow blessedness and He takes holy pleasure in the happiness of His people.”</p>

<p>QUES: How has God revealed His goodness towards you?</p>

<p>1.</p>

<p>2.</p>

<p>3.</p>

<p>4.</p>

<p>If your list is like mine it is often circumstantial (health, means, reliable vehicle, etc). God’s goodness however, is to be based on at least two truths:</p>

<ol>
	<li>His character - Just, holy, loving, impartial, righteous, etc.

		<ol start="2">
			<li>His plan for you and me - Listen to Tozer once again:

				<p>“With the goodness of god to desire our highest welfare, the wisdom of God to plan it, and the power of God to achieve it, what do we lack?”</p></li>
		</ol></li>
</ol>

<p>You see folks, in this journey we call life God uses all circumstances, some of which He specifically implements, to “conform us to the image of His Son.” Our Father is always chiseling, honing and shaping us to be more like Jesus. Just the fact that He sees, care’s and has a plan for my life is indeed what reveals that He is a good, good God.</p>

<p>“For the Lord God is a sun and shield: The Lord will give grace and glory: no good thing will He withhold from them that walk uprightly.” Ps 84:11</p>

