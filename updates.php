<h2>July 02, 2020</h2>

<p>Subject: Pastor’s Devotional</p>

<p>SIGNS OF LIFE...Back to the basics of authentic Christianity </p>

<p><em>Dr. David Jeremiah</em></p>

<blockquote>
<p>”You are the __ of the. A city that is set on a hill cannot be hidden. Nor do they ____ a __ and put it under a basket, but on a lampstand, and it gives lights to all who are in the house.&quot; - Matt 5:14-15</p>
</blockquote>

<p>I. We are the light of the world</p>

<p>A. Light destroys darkness — John 1:5</p>

<p>• Salt holds back evil and its destruction, but light shines forth that which is good. We are light — manifesting righteousness — As we shine forth the light of Christ in this world, darkness will be driven back.</p>

<p>Ques: Why do you think evil seems to triumph so much in our world today?</p>

<p>B. Light detects evil — John 3:19-20</p>

<p>• Evil deeds of the world are illuminated by the presence of Christians in the world. In other words, the light of Christ is the standard, or benchmark, for righteousness.</p>

<p>• Without the light of Christ in the church, the evil deeds of the world would have nothing against which to be measured.</p>

<p>C. Light discovers good — John 3:21</p>

<p>• When those who are responding to the light come closer to the light of Christ present in the church, their deeds become clearly visible.</p>

<p>• It becomes obvious that God is at work in their life.</p>

<p>D. Light defines Christianity — Eph 5:8</p>

<p>• The Apostle Paul stated: though we were once we are now __ in the Lord, and should walk as __ of __</p>

<p>• We have been called as children of light — and light is the essence of Christianity.</p>

<p>• The darker the world around us gets, the brighter our light should shine.</p>

<p>Ques: How can our light shine bright in a sin darkened world?</p>

<p>E. Light demands exposure — Matt 5:14-16</p>

<p>• The purpose of light is to illuminate</p>

<p>• If our light is concealed by the fact that we stay home or stay at church and never let the world see our light, what is accomplished?</p>

<p>• For light to accomplish its purpose, it must leave the presence of other lights and go out into the darkness (IN the world but not the world)</p>

<p>Summation: When salt loses its flavor, Jesus said, it is worthless. And when a light is put under a basket,</p>

<p>it too is worthless. For the church to accomplish its purpose of PRESERVATION and ILLUMINATION we must go out into the world where decay and darkness are present. YOU can be as effective as one grain of salt and one candle — if only you will go where you are needed.</p>

<p>Happy Studying!</p>


